package java_basic.variables;

public class Variables {
    public static void main(String[] args) {

        int x = 34;
        System.out.println(x);

        x = 23;
        System.out.println(x * 3);

        String words = "Isso é uma sentença";
        System.out.println(words + ", Isso aqui é uma sentença");

    }
}
